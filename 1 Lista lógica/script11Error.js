// ler as dimensões de um trapézio (base maior, base menor e altura), calcular e escrever a área do trapézio.

let baseMaior = parseInt (prompt("Insira a base maior do trapézio"));

let baseMenor = parseInt (prompt("Insira a base menor do trapézio"));

let altura = parseInt (prompt("Insira a altura do trapézio"));

let area = (baseMaior*baseMenor)*altura/2

alert(area);